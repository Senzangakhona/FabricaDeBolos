
public abstract class AbstractBolo {
	private String nome;
	private String tipo;
	private Float preco;
	
	public void sobreoBolo() {
		
	}
	
	public void receita() {}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	public void setPreco(float preco) {
		this.preco = preco;
	}
	
}
